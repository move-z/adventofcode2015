class Present:
    def __init__(self, l, w, h):
        self.l = l
        self.w = w
        self.h = h

    def area(self):
        return 2 * (self.l * self.w + self.l * self.h + self.w * self.h)

    def slack(self):
        return min(self.l * self.w, self.l * self.h, self.w * self.h)

    def ribbon(self):
        return 2 * min(self.l + self.w, self.l + self.h, self.w + self.h) + self.l * self.w * self.h


def first(*presents):
    return sum([p.area() + p.slack() for p in presents])


def second(*presents):
    return sum([p.ribbon() for p in presents])


